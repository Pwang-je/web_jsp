package Bean_pack;

public class ProductBean {

    private String name;
    private int price;
    private int disc;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getPrice() {
        return price;
    }

    public void setDisc(int disc) {
        this.disc = disc;
    }

    public int getDisc() {
        return disc;
    }
}
